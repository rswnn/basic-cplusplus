#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    // Math.max() to find max number
    cout << max(10, 5) << "\n";

    // Math.min() to find min number
    cout << min(10, 5) << "\n";

    // Math.sqrt() to find square root
    cout << sqrt(64) << "\n";

    // Math.round() to find rounds a number
    cout << round(2.6) << "\n";

    // Math.log() to find natural logarithm
    cout << log(2) << "\n";

    return 0;
}